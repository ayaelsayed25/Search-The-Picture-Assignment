import java.io.*;
import java.lang.*;
import java.util.*;
public class MyPoint {
    int x;
    int y;
    public MyPoint(int x, int y)
    {
        this.x = x;
        this.y = y;
    }
    public int GetX()
    {
        return x;
    }
    public int GetY()
    {
        return y;
    }

}
